public class Licz {
    int pierwsza, druga, wynik;

    public int sumuj(int pierwsza, int druga) {
        this.pierwsza = pierwsza;
        this.druga = druga;
        this.wynik = wynik;
        wynik = pierwsza + druga;
        return wynik;
    }

    public int kwadratLiczby(int pierwsza) {
        this.pierwsza = pierwsza;
        this.wynik = wynik;
        wynik = pierwsza * pierwsza;
        return wynik;
    }

}
