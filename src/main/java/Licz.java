public class Licz {
    long pierwsza, druga, wynik;

    public long sumuj(long pierwsza, long druga) {
        this.pierwsza = pierwsza;
        this.druga = druga;
        this.wynik = wynik;
        wynik = pierwsza + druga;
        return wynik;
    }

    public long kwadratLiczby(long pierwsza) {
        this.pierwsza = pierwsza;
        this.wynik = wynik;
        wynik = pierwsza * pierwsza;
        return wynik;
    }

}
